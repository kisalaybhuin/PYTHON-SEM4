a=int(input("Enter 1st value:"))
b=int(input("Enter 2nd value:"))
op=input("Enter Arithmetic operator:")
if op=='+':
    print(" Addition is:",a+b)
if op=='-':
    print(" Subtraction is:",a-b)
if op=='*':
    print(" Multiplication is:",a*b)
if op=='/':
    print(" Division is:",a/b)
 
